#%%
def get_proxies(n=20):
    ''' Grab a list of 20 elite proxies from free-proxy-list.net
    '''
    url = 'https://free-proxy-list.net/'
    response = requests.get(url)
    parser = html.fromstring(response.text)
    proxies = set()
    n_proxies = 0
    i_proxy = 0

    while n_proxies < n:
        i = parser.xpath('//tbody/tr')[i_proxy]
        if i.xpath('.//td[5][contains(text(),"elite")]'):
            #Grabbing IP and corresponding PORT
            proxy = ":".join([i.xpath('.//td[1]/text()')[0], i.xpath('.//td[2]/text()')[0]])
            proxies.add(proxy)
            n_proxies += 1
        i_proxy += 1

    return list(proxies)

def get_random_user_agent():
    user_agent = random.choice(user_agent_list)
    #Set the headers
    headers = {'User-Agent': user_agent}
    return headers

#%%
class Scraper:

    def __init__(self):
        self.proxies = get_proxies()

    def get_request(self, url, **kwargs):
        ''' Wrapper for request.get using a random proxy and user agent.
        '''
        headers = get_random_user_agent()
        success = False
        while not success:
            proxy = random.choice(self.proxies)
            try:
                response = requests.get(url, headers=headers,
                    proxies={"http": proxy, "https": proxy},
                    **kwargs)
                success = True
            except (ssl.SSLError, requests.exceptions.ProxyError) as e:
                self.proxies.remove(proxy)

                if len(self.proxies) == 0:
                    self.proxies = get_proxies()

        return response
